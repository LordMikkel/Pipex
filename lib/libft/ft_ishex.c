#include "../libft_plus.h"

int	ft_ishex(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'F')
		|| (c >= 'a' && c <= 'f') || c == 'x' || c == 'X')
		return (1);
	return (0);
}
